# Sistema que roda o bot dentro da pasta e tem o resultado para ser usado

from subprocess import Popen, PIPE, CalledProcessError

with Popen(['python', 'bot_files/bot.py'], stdout=PIPE, bufsize=1, universal_newlines=True, encoding="utf8") as p:
    for line in p.stdout:
        print(line, end='') # process line here

if p.returncode != 0:
    raise CalledProcessError(p.returncode, p.args)